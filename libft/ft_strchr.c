#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*str;

	if (c >= 255)
		(c -= 256);
	if (!ft_isascii(c))
		return (0);
	str = (char *)s;
	while (*str != c)
	{
		if (*str == '\0')
			return (NULL);
		str++;
	}
	return (str);
}

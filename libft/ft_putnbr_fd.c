#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	a;

	if (fd < 0)
		return ;
	if (n == -2147483648)
	{
		write (fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write (fd, "-", 1);
		n = n * -1;
	}
	if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		a = '0' + (n % 10);
	}
	if (n < 10)
		a = '0' + n;
	write (fd, &a, 1);
}
